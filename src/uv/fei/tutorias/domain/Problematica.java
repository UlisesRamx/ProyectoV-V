
package uv.fei.tutorias.domain;

/**
 *
 * @author Valea
 */
public class Problematica {
    int idproblemaacademica;
    String titulo;
    String descripcion;
    String solucion;
    String experiencia;
    String docente;
    String programa;
    String fechatutoria;

    public int getIdproblemaacademica() {
        return idproblemaacademica;
    }

    public void setIdproblemaacademica(int idproblemaacademica) {
        this.idproblemaacademica = idproblemaacademica;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getSolucion() {
        return solucion;
    }

    public void setSolucion(String solucion) {
        this.solucion = solucion;
    }

    public String getExperiencia() {
        return experiencia;
    }

    public void setExperiencia(String experiencia) {
        this.experiencia = experiencia;
    }

    public String getDocente() {
        return docente;
    }

    public void setDocente(String docente) {
        this.docente = docente;
    }

    public String getPrograma() {
        return programa;
    }

    public void setPrograma(String programa) {
        this.programa = programa;
    }

    public String getFechatutoria() {
        return fechatutoria;
    }

    public void setFechatutoria(String fechatutoria) {
        this.fechatutoria = fechatutoria;
    }
    
  
}
